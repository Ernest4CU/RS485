uint16 crc16(uint8 *puchMsg, uint16 usDataLen)
{
	uint8 uchCRCHi = 0xFF ; /* 高CRC字节初始化 */
	uint8 uchCRCLo = 0xFF ; /* 低CRC 字节初始化 */
	uint32 uIndex ; /* CRC循环中的索引 */

	while (usDataLen--) /* 传输消息缓冲区 */
	{
		uIndex = uchCRCHi ^ *puchMsg++ ; /* 计算CRC */
		uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex] ;
		uchCRCLo = auchCRCLo[uIndex] ;
	}
	return (uchCRCHi << 8 | uchCRCLo) ;
}

void reactOfcmd01(uint8 *cmd)
{
	uint16 add,cnt;
	add=*cmd;
	add=add<<8|*(cmd+1);
	cnt=*(cmd+2);
	cnt=cnt|*(cmd+3);
	if(add!=0)
	{
		set_uart1_tx_buff(0x0f|0x80,1);
		set_uart1_tx_buff(0x02,2);
		set_uart1_tx_crc(3,3);
		Os_uart1_rsp_len=5;
		return;
	}
	else{
		set_uart1_tx_buff(0x0f,1); //功能码
		Os_uart1_rsp_len=2;
	}
	switch(add){
	case 0://唯一就一组led
		if(cnt==8){
			set_uart1_tx_buff(1,2);
			set_uart1_tx_buff(P6IN,3);
			set_uart1_tx_crc(4,4);
			Os_uart1_rsp_len=6;
		}
		break;
	default:
		break;
	}
}


