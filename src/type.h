#ifndef SRC_TYPE_H_
#define SRC_TYPE_H_

/********************************************************************************************************
*                       Date types(Compiler specific)  数据类型（和编译器相关）                         *
********************************************************************************************************/
typedef unsigned char   uint8;          // Unsigned  8 bit quantity  无符号8位整型变量
typedef signed   char   int8;           // Signed    8 bit quantity  有符号8位整型变量
typedef unsigned int  uint16;         // Unsigned 16 bit quantity  无符号16位整型变量
typedef signed   int  int16;          // Signed   16 bit quantity  有符号16位整型变量
typedef unsigned long    uint32;         // Unsigned 32 bit quantity  无符号32位整型变量
typedef signed   long    int32;          // Signed   32 bit quantity  有符号32位整型变量
typedef float           fp32;           // Single precision floating point 单精度浮点数（32位长度）
typedef double          fp64;           // Double precision floating point 双精度浮点数（64位长度）



#endif /* SRC_TYPE_H_ */
